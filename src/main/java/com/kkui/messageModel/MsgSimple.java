package com.kkui.messageModel;

/**
 * @Author kkui
 * @Date 2022/8/18
 * @Description 文字 + 链接 消息类型
 */
public class MsgSimple {
    private String name;
    private String link;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
